package com.atguigu;

public class Person {
    private  String pNAME;
    private  int pAGE;
    private  String pSEX;

    public String getNAME() {
        return pNAME;
    }

    public void setNAME(String pNAME) {
        this.pNAME = pNAME;
    }

    public int getAGE() {
        return pAGE;
    }

    public void setAGE(int pAGE) {
        this.pAGE = pAGE;
    }

    public String getSEX() {
        return pSEX;
    }

    public void setSEX(String pSEX) {
        this.pSEX = pSEX;
    }
}
